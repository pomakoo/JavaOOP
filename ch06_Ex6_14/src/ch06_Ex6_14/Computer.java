package ch06_Ex6_14;

public class Computer {
	private int no;
	private String spect;
	private Room room;
	
	public int getNo() {
		return no;
	}
	
	public void setNo(int no) {
		this.no = no;
	}
	
	public String getSpect() {
		return spect;
	}
	
	public void setSpect(String spect) {
		this.spect = spect;
	}
	
	public Room getRoom() {
		return room;
	}
	
	public void setRoom(Room room) {
		this.room = room;
	}
}
